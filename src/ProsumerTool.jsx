function runOpt(load, totalLoad, p, c) {
  const{kwpMin,kwpMax,kwhMin,kwhMax,minROI}=c;
  const kvs=[],khs=[];
  for(let k=kwpMin;k<=kwpMax+1e-9;k+=0.5)kvs.push(Math.round(k*10)/10);
  for(let b=kwhMin;b<=kwhMax+1e-9;b+=0.5)khs.push(Math.round(b*10)/10);
  const pvRng=mkRng(p.weatherSeed||42);
  const pvShape=genPV(p.pvSpezErtrag,pvRng);
  const spotScale=p.spotScale||1.0;
  const spot=EPEX_2025_CT.map(v=>v*spotScale);
  const all=[];
  for(const kwp of kvs)for(const bk of khs)all.push(evalCfg(load,totalLoad,kwp,bk,p,pvShape,spot));
  const inB=all.filter(r=>r.kwp>=kwpMin&&r.kwp<=kwpMax&&r.battKWh>=kwhMin&&r.battKWh<=kwhMax);
  const mxN=inB.reduce((a,b)=>b.nettoErsparnis>a.nettoErsparnis?b:a,{nettoErsparnis:-Infinity});
  const mxR=inB.filter(r=>r.totalInvest>0).reduce((a,b)=>b.roi>a.roi?b:a,{roi:-Infinity});
  const wirt=inB.filter(r=>r.nettoErsparnis>=0);
  const mxA=wirt.length>0?wirt.reduce((a,b)=>b.autarkie>a.autarkie?b:a):null;
  const sorted=[...inB].sort((a,b)=>a.totalInvest-b.totalInvest);
  const pareto=[];let bCF=-Infinity;
  for(const r of sorted){const cf=r.ersparnis-(r.totalInvest*p.betriebskostenAnteil);if(cf>bCF){pareto.push({...r,cashflow:cf});bCF=cf;}}
  let kp=null,sh=null,el=null;
  if(pareto.length>=3){
    const fr=pareto.filter(x=>x.totalInvest>0);
    if(fr.length>=3){
      const xMn=fr[0].totalInvest,xMx=fr[fr.length-1].totalInvest,yMn=fr[0].cashflow,yMx=fr[fr.length-1].cashflow;
      kp=fr.map(x=>({...x,kd:((x.cashflow-yMn)/(yMx-yMn||1))-((x.totalInvest-xMn)/(xMx-xMn||1))})).reduce((a,b)=>b.kd>a.kd?b:a);
      const ah=fr.filter(x=>x.cashflow-minROI*x.totalInvest>0);
      sh=ah.length>0?ah.map(x=>({...x,sa:(x.cashflow-minROI*x.totalInvest)/Math.sqrt(x.totalInvest)})).reduce((a,b)=>b.sa>a.sa?b:a):null;
      el=fr[fr.length-1];
      for(let i=1;i<fr.length;i++){const pv=fr[i-1],cv=fr[i];if(pv.totalInvest<=0||pv.cashflow<=0)continue;const dLX=Math.log(cv.totalInvest)-Math.log(pv.totalInvest),dLY=Math.log(Math.max(0.01,cv.cashflow))-Math.log(Math.max(0.01,pv.cashflow));if(dLX>0&&dLY/dLX<0.5){el=pv;break;}el=cv;}
    }
  }
  const marginal=[];
  for(let i=1;i<pareto.length;i++){const pv=pareto[i-1],cv=pareto[i],dI=cv.totalInvest-pv.totalInvest,dC=cv.cashflow-pv.cashflow;marginal.push({totalInvest:cv.totalInvest,kwp:cv.kwp,battKWh:cv.battKWh,marginalROI:dI>0?dC/dI:0,cashflow:cv.cashflow,deltaInvest:dI,deltaCashflow:dC});}
  const strats=[
    {id:'knee',label:'Bester Kompromiss',group:'pareto',color:'#22d3ee',shape:'diamond',cfg:kp,desc:'Sweet Spot zwischen Investition und Ertrag.',expl:'Kneedle-Algorithmus: Kniepunkt der Pareto-Front.'},
    {id:'sharpe',label:'Beste Effizienz',group:'pareto',color:'#fbbf24',shape:'triangle',cfg:sh,desc:'Bestes Verhältnis Mehrertrag/Mehraufwand.',expl:'Sharpe-Analog: (Cashflow - rf×Invest) / √Invest.'},
    {id:'elast',label:'Sicher investiert',group:'pareto',color:'#a78bfa',shape:'square',cfg:el,desc:'Stoppt wo Grenznutzen < 0.5.',expl:'Elastizitätsabfall in log-log-Darstellung.'},
    {id:'maxN',label:'Höchster Gewinn',group:'extrem',color:'#10b981',shape:'star',cfg:mxN.totalInvest>-Infinity?mxN:null,desc:'Max. absoluter Jahresgewinn.',expl:''},
    {id:'maxR',label:'Beste Verzinsung',group:'extrem',color:'#06b6d4',shape:'wye',cfg:mxR.roi>-Infinity?mxR:null,desc:'Max. Rendite auf Kapital.',expl:''},
    {id:'maxA',label:'Größte Unabhängigkeit',group:'extrem',color:'#f472b6',shape:'circle',cfg:mxA,desc:'Höchste Autarkie ohne Jahresverlust.',expl:''},
  ];
  return{all:inB,pareto,marginal,strats};
}
function evalCfg(load, totalLoad, kwp, battKWh, p, pvShape, spot) {
  const pv=kwp>0?pvShape.map(v=>v*kwp):new Array(8760).fill(0);
  const res=simulate(load,pv,battKWh,spot,p.batterieStrategie||'eigenverbrauch',p.tarifModus||'fest');
  const tPV=kwp*p.pvSpezErtrag,tIm=sum(res.gridImport),tEx=sum(res.gridExport);
  const ev=tPV-tEx,aut=totalLoad>0?(totalLoad-tIm)/totalLoad:0;
  const pvI=kwp*p.pvKostenProKWp,spI=battKWh*p.speicherKostenProKWh,tI=pvI+spI;
  const pvA=kwp>0?pvI*annuity(p.zinssatz,p.pvLebensdauer):0;
  const spA=battKWh>0?spI*annuity(p.zinssatz,p.speicherLebensdauer):0;
  const bk=tI*p.betriebskostenAnteil;
  const kO=calcStromkosten(load,spot,p.tarifModus,p.strompreis);
  const kM=calcStromkosten(res.gridImport,spot,p.tarifModus,p.strompreis);
  const eins=calcEinspeise(res.gridExport,pv,p.einspeiseverguetung,p.einspeiseModus||'fest');
  const ers=kO-(kM-eins),nErs=ers-pvA-spA-bk;
  const cfY=ers-bk,amort=cfY>0?tI/cfY:Infinity,roi=tI>0?cfY/tI:0;
  return{kwp,battKWh,autarkie:aut,totalImport:tIm,totalExport:tEx,eigenverbrauch:ev,totalInvest:tI,nettoErsparnis:nErs,ersparnis:ers,amortisation:amort,roi};
}

