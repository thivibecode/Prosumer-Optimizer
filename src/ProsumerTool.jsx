// ============================================================
// SIMULATION: Eigenverbrauch oder Arbitrage
//
// ARBITRAGE-ALGORITHMUS (wissenschaftlich fundiert):
// Basiert auf dem "Greedy Spread"-Ansatz aus der Literatur
// (Abramova & Bunn 2021, MDPI 2024, Krishnamurthy 2017):
//
// Täglich werden aus dem 24h-Preisvektor (Day-Ahead) optimale
// Lade- und Entladezeitpunkte berechnet:
//
// 1. TAGESPLANUNG (voller Day-Ahead-Lookahead):
//    - Berechne für jede Stunde den erwarteten Eigenverbrauch
//    - Residuallast = Load - PV (ohne Speicher)
//    - Für Stunden mit Netto-PV-Überschuss: Speicher lädt sowieso
//    - Für restliche Stunden: Greedy-Spread-Optimierung
//
// 2. GREEDY SPREAD (nach Abramova & Bunn 2021):
//    - Sortiere Stunden nach Preis aufsteigend
//    - Bestimme Lade-Schwellenwert: niedrigste Stunden bis Kapazität voll
//    - Bestimme Entlade-Schwellenwert: höchste Stunden bis Kapazität leer
//    - Berücksichtige Roundtrip-Effizienz: Laden lohnt nur wenn
//      p_entladen > p_laden / eff (Effizienz-Hurdle)
//
// 3. SOC-CONSTRAINTS (hart):
//    - Nie über Kapazität laden
//    - Nie unter 0 entladen
//    - C-Rate: max 0.5C (max 30 min Voll-/Entladung)
//    - Eigenverbrauch hat immer Priorität vor Arbitrage
// ============================================================

// ============================================================
// SIMULATION - physikalisch korrekte Regeln
//
// PV-HIERARCHIE (unveränderlich, jede Stunde):
//   1. PV -> Eigenverbrauch
//   2. PV-Überschuss -> Speicher laden
//   3. Speicher-Rest -> Netz (nur PV-Einspeisung, nie Speicher-Entladung)
//
// SPEICHER-REGELN:
//   - Entlädt ausschließlich für Eigenverbrauch (nie ins Netz)
//   - Lädt aus PV-Überschuss (immer, Priorität)
//   - Lädt zusätzlich aus Netz wenn:
//       - dynamischer Tarif aktiv
//       - Preis günstig (Roundtrip-Gewinn > 0, inkl. Effizienz-Hurdle)
//       - SOC-Reserve für erwartete PV-Produktion freigehalten
//         (verhindert dass PV-Strom ins Netz muss weil Speicher voll)
//
// ARBITRAGE-ALGORITHMUS (Spread-Trading nach Abramova & Bunn 2021):
//   Tägliche Vorausplanung mit vollem 24h-Forecast:
//   1. Baseline-SOC: simuliere Tag ohne Arbitrage (nur EV)
//   2. Netz-Laden: für jede Stunde h_i prüfe ob spätere Stunde h_j>h_i
//      existiert mit Nettogewinn (p_j - p_i)xeff - Effizienzverlust > 0.3ct
//      Lade-Limit: battKWh - Reserve(PV-Erwartung ab h_i+1)
//   3. Backward-DP Entladung: teuerste Bedarfsstunden zuerst,
//      SOC-Verfügbarkeit wird laufend adjustiert
//
// KORREKTHEIT-CHECKS:
//   [OK] PV-Strom immer zuerst für Eigenverbrauch
//   [OK] Speicher nie ins Netz
//   [OK] Netz-Laden nur bei dyn. Tarif + positivem Spread
//   [OK] SOC-Reserve für PV verhindert Einspeisung-Verluste
//   [OK] Backward-DP: SOC-Adjustierung nach jeder Entlade-Zuweisung
// ============================================================

function planTagOptimal(load24, pv24, spot24, battKWh, eff, socStart, dynTarif) {
  const sqE  = Math.sqrt(eff);
  const maxP = battKWh * 0.5;  // 0.5C max Lade-/Entladeleistung

  // Residuallast: >0 = PV-Überschuss, <0 = Netz-Bedarf
  const resid = load24.map((l, h) => pv24[h] - l);

  // Erwarteter zukünftiger PV-Überschuss (kumuliert rückwärts)
  // pvRem[h] = noch erwartete PV-Überschuss-kWh ab Stunde h
  const pvRem = new Array(25).fill(0);
  for (let h = 23; h >= 0; h--) {
    pvRem[h] = pvRem[h + 1] + Math.max(0, resid[h]);
  }

  // === SCHRITT 1: Baseline-SOC (nur Eigenverbrauch, kein Netz-Laden) ===
  const socBL = new Array(25);
  socBL[0] = socStart;
  for (let h = 0; h < 24; h++) {
    let s = socBL[h];
    if (resid[h] >= 0) {
      const ch = Math.min(resid[h], (battKWh - s) / sqE, maxP);
      s = Math.min(battKWh, s + ch * sqE);
    }
    socBL[h + 1] = s;
  }

  // === SCHRITT 2: Netz-Laden planen (Spread-Trading) ===
  const netzLade = new Array(24).fill(0);

  if (dynTarif) {
    for (let hi = 0; hi < 24; hi++) {
      const p_i = spot24[hi];

      // SOC-Reserve: lasse Platz für erwarteten PV-Überschuss der nächsten Stunden
      // Mindest-Reserve: 15% Puffer + erwartete PV-Energie (gedämpft mit eff)
      const pvReserve = Math.min(battKWh * 0.85, pvRem[hi + 1] * sqE);
      const maxSOCtarget = battKWh - Math.max(battKWh * 0.10, pvReserve);

      // Prüfe ob profitable Entlade-Stunde h_j > h_i existiert
      // Bedingung (Spread-Trading): (p_j - p_i) x eff > |p_i| x (1/eff - 1)
      // = Entlade-Gewinn x eff > Effizienz-Verlust beim Laden
      let bestGain = 0;
      for (let hj = hi + 1; hj < 24; hj++) {
        if (resid[hj] < 0) {  // hj muss Bedarfsstunde sein
          const gain = (spot24[hj] - p_i) * eff - Math.max(0, p_i) * (1 / eff - 1);
          if (gain > bestGain) bestGain = gain;
        }
      }

      // Lade wenn: Nettogewinn > 0.3 ct/kWh Mindestmarge
      if (bestGain > 0.3) {
        const socAtHi  = socBL[hi + 1];  // SOC nach Baseline (inkl. PV-Laden)
        const ladeRaum = Math.max(0, maxSOCtarget - socAtHi);
        const ch = Math.min(maxP, ladeRaum / sqE);
        if (ch > 0.05) {
          netzLade[hi] = ch;
          // Propagiere SOC-Erhöhung für spätere Reserve-Berechnungen
          for (let hh = hi + 1; hh < 25; hh++) {
            socBL[hh] = Math.min(battKWh, socBL[hh] + ch * sqE);
          }
        }
      }
    }
  }

  // === SCHRITT 3: Forward-SOC mit Netz-Laden ===
  const socFwd = new Array(25);
  socFwd[0] = socStart;
  for (let h = 0; h < 24; h++) {
    let s = socFwd[h];
    // PV-Laden hat immer Priorität
    if (resid[h] >= 0) {
      const ch = Math.min(resid[h], (battKWh - s) / sqE, maxP);
      s = Math.min(battKWh, s + ch * sqE);
    }
    // Netz-Laden (zusätzlich, nur wenn noch Raum nach PV-Laden)
    if (netzLade[h] > 0) {
      const ch = Math.min(netzLade[h], (battKWh - s) / sqE);
      netzLade[h] = ch;  // actual achievable amount
      s = Math.min(battKWh, s + ch * sqE);
    }
    socFwd[h + 1] = s;
  }

  // === SCHRITT 4: Backward-DP Entladung ===
  // Alle Bedarfsstunden, teuerste zuerst.
  // SOC-Adjustierung nach jeder Zuweisung verhindert Doppelvergabe.
  const bedarfSorted = [];
  for (let h = 0; h < 24; h++) {
    if (resid[h] < 0) bedarfSorted.push({ h, preis: spot24[h], kwh: -resid[h] });
  }
  bedarfSorted.sort((a, b) => b.preis - a.preis);

  const entladeZiel = new Array(24).fill(0);
  const socAdj = socFwd.slice();

  for (const { h, kwh } of bedarfSorted) {
    const avail  = socAdj[h + 1];
    const output = Math.min(kwh, avail * sqE, maxP);
    entladeZiel[h] = output;
    const consumed = output / sqE;
    for (let hh = h + 1; hh < 25; hh++) {
      socAdj[hh] = Math.max(0, socAdj[hh] - consumed);
    }
  }

  // === SCHRITT 5: SOC-Ende des Tages (Startwert für nächsten Tag) ===
  let soc = socStart;
  for (let h = 0; h < 24; h++) {
    if (resid[h] >= 0) {
      const ch = Math.min(resid[h], (battKWh - soc) / sqE, maxP);
      soc = Math.min(battKWh, soc + ch * sqE);
    }
    if (netzLade[h] > 0) {
      soc = Math.min(battKWh, soc + netzLade[h] * sqE);
    }
    if (entladeZiel[h] > 0) {
      const dp = Math.min(entladeZiel[h] / sqE, soc, maxP);
      soc = Math.max(0, soc - dp);
    }
  }

  return { entladeZiel, netzLade, socEnd: soc };
}

function simulate(load, pv, battKWh, spotPrices=null, strat='eigenverbrauch', tarifModus='fest') {
  const n    = load.length;
  const eff  = 0.92;
  const maxP = battKWh * 0.5;
  const sqE  = Math.sqrt(eff);

  const soc = new Array(n).fill(0);
  const gi  = new Array(n).fill(0);   // Netzbezug
  const ge  = new Array(n).fill(0);   // Netzeinspeisung (nur PV)
  const bc  = new Array(n).fill(0);   // Speicher-Laden
  const bd  = new Array(n).fill(0);   // Speicher-Entladen

  const isDyn = tarifModus === 'dynamisch';
  let curSoc = battKWh > 0 ? battKWh * 0.2 : 0;

  // Arbitrage: Tagesplanung mit 24h-Forecast vorberechnen
  let plan = null;
  if (strat === 'arbitrage' && battKWh > 0 && spotPrices) {
    const ezArr = new Array(n).fill(0);
    const nlArr = new Array(n).fill(0);
    let socFwd = curSoc;
    for (let d = 0; d < 365; d++) {
      const i0   = d * 24;
      const l24  = Array.from({ length: 24 }, (_, h) => load[i0 + h]);
      const p24  = Array.from({ length: 24 }, (_, h) => pv[i0 + h]);
      const s24  = Array.from({ length: 24 }, (_, h) => spotPrices[i0 + h]);
      const { entladeZiel, netzLade, socEnd } = planTagOptimal(l24, p24, s24, battKWh, eff, socFwd, isDyn);
      for (let h = 0; h < 24; h++) {
        ezArr[i0 + h] = entladeZiel[h];
        nlArr[i0 + h] = netzLade[h];
      }
      socFwd = socEnd;
    }
    plan = { ez: ezArr, nl: nlArr };
  }

  // Stündliche Simulation
  for (let i = 0; i < n; i++) {
    const netPV   = pv[i] - load[i];
    const netzCh  = plan ? plan.nl[i] : 0;
    const entZiel = plan ? plan.ez[i] : 0;

    if (netPV >= 0) {
      // ── PV-Überschuss-Stunde ─────────────────────────────
      // 1. PV deckt Last
      // 2. Überschuss -> Speicher
      const chPV = battKWh > 0 ? Math.min(netPV, (battKWh - curSoc) / sqE, maxP) : 0;
      curSoc = Math.min(battKWh, curSoc + chPV * sqE);
      // 3. Netz-Laden (Arbitrage, auch wenn PV da: Reserve war eingeplant)
      const chN = battKWh > 0 && netzCh > 0
        ? Math.min(netzCh, (battKWh - curSoc) / sqE, maxP) : 0;
      curSoc = Math.min(battKWh, curSoc + chN * sqE);
      bc[i] = chPV + chN;
      gi[i] = chN;                              // Netzbezug nur für Netz-Laden
      ge[i] = Math.max(0, netPV - chPV);       // PV-Überschuss nach Speicher → Netz
      bd[i] = 0;

    } else {
      // ── Bedarfs-Stunde ────────────────────────────────────
      const bedarf = -netPV;

      // A. Netz-Laden (Arbitrage): lade zusätzlich für spätere teure Stunden
      const chN = battKWh > 0 && netzCh > 0
        ? Math.min(netzCh, (battKWh - curSoc) / sqE, maxP) : 0;
      if (chN > 0) curSoc = Math.min(battKWh, curSoc + chN * sqE);

      // B. Speicher entlädt für Eigenverbrauch
      //    Eigenverbrauch: immer sofort
      //    Arbitrage: nach Plan (teuerste Stunden bevorzugt)
      const ziel = strat === 'arbitrage' ? entZiel : bedarf;
      const disch = battKWh > 0 ? Math.min(ziel / sqE, curSoc, maxP) : 0;
      curSoc = Math.max(0, curSoc - disch);

      // C. Rest aus Netz
      bc[i] = chN;
      bd[i] = disch * sqE;
      gi[i] = Math.max(0, bedarf - bd[i]) + chN;
      ge[i] = 0;
    }

    soc[i] = Math.max(0, Math.min(battKWh, curSoc));
  }

  return { soc, gridImport: gi, gridExport: ge, batteryCharge: bc, batteryDischarge: bd };
}
function calcStromkosten(gridImport, spotPrices, modus, festpreis) {
  if(modus==='fest'||!spotPrices) return sum(gridImport)*festpreis;
  let k=0;for(let i=0;i<gridImport.length;i++) k+=gridImport[i]*spotToEK(spotPrices[i])/100;
  return k+DYN_FEE_PA;
}
function calcEinspeise(gridExport, pv, vergut, modus) {
  if(modus==='nie')return 0;if(modus==='fest')return sum(gridExport)*vergut;
  const sv=[...pv].sort((a,b)=>b-a),cut=sv[Math.floor(sv.length*0.07)];
  let e=0;for(let i=0;i<gridExport.length;i++)if(gridExport[i]>0&&pv[i]<cut)e+=gridExport[i]*vergut;
  return e;
}

