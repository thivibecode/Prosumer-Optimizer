function aggMonthly({load,pv,gridImport,gridExport,batteryDischarge}) {
  const mD=[31,28,31,30,31,30,31,31,30,31,30,31];
  const mN=['Jan','Feb','Mrz','Apr','Mai','Jun','Jul','Aug','Sep','Okt','Nov','Dez'];
  let dc=0;
  return mD.map((days,m)=>{
    let l=0,p=0,gi=0,ge=0,bd=0;
    for(let d=0;d<days;d++)
      for(let h=0;h<24;h++){
        const i=(dc+d)*24+h;
        if(i<load.length){l+=load[i];p+=pv[i];gi+=gridImport[i];ge+=gridExport[i];if(batteryDischarge)bd+=batteryDischarge[i];}
      }
    dc+=days;
    return{month:mN[m],load:Math.round(l),pv:Math.round(p),gridImport:Math.round(gi),gridExport:Math.round(ge),
           direktPV:Math.round(Math.max(0,l-gi-bd)),speicher:Math.round(bd),netz:Math.round(gi)};
  });
}

