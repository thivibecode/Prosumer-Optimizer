function aggDaily({load,pv,gridImport,gridExport,soc,batteryCharge,batteryDischarge,haushalt,waerme,ev}) {
  const r=[];
  for(let d=0;d<365;d++){
    let l=0,p=0,gi=0,ge=0,bc=0,bd=0,sc=0,h_=0,w_=0,e_=0;
    for(let h=0;h<24;h++){
      const i=d*24+h;
      l+=load[i];p+=pv[i];gi+=gridImport[i];ge+=gridExport[i];
      bc+=batteryCharge[i];bd+=batteryDischarge[i];sc=soc[i];
      h_+=haushalt[i];w_+=waerme[i];e_+=ev[i];
    }
    r.push({day:d+1,load:l,pv:p,gridImport:gi,gridExport:ge,soc:sc,haushalt:h_,waerme:w_,ev:e_});
  }
  return r;
}

